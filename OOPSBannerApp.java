//OOPSBannerApp UC3 - Banner Display using String Join
public class OOPSBannerApp{ 
       public static void main(String[] arg){
            System.out.println(String.join(" ", "   ***   ", "   ***   ", "******   ", "  *****  "));
			System.out.println(String.join(" ", " **   ** ", " **   ** ", "**    ** ", " **   ** "));
			System.out.println(String.join(" ", "**     **", "**     **", "**     **", "**       "));
			System.out.println(String.join(" ", "**     **", "**     **", "**    ** ", " **      "));
			System.out.println(String.join(" ", "**     **", "**     **", "******   ", "  *****  "));
			System.out.println(String.join(" ", "**     **", "**     **", "**       ", "      ** "));
			System.out.println(String.join(" ", "**     **", "**     **", "**       ", "       **"));
			System.out.println(String.join(" ", " **   ** ", " **   ** ", "**       ", " **   ** "));
			System.out.println(String.join(" ", "   ***   ", "   ***   ", "**       ", "  *****  "));

       }
}